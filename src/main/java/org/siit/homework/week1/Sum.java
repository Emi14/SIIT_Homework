package org.siit.homework.week1;

public class Sum {

    // Calculate the sum of the first 100 numbers higher than 0 //
    public void sum() {
        int sum = 0;
        for (int i = 0; i < 100; i++) {
            sum += i;

        }
        System.out.println("The Sum of the first 100 numbers higher than 0 = " + sum);

    }
}
